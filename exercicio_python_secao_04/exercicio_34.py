"""
34. Leia o valor do raio de um círculo e calcule e imprima a área do círculo correspondente.
A área do circulo é 𝝿 * raio2, considere  𝝿 = 3.141592.
"""

print('* DESCUBRA A ÁREA DE UM CÍRCULO *')
raio = float(input('Digite o valor do Raio de um Círculo:'))

area = 3.14 * (raio ** 2)

print(f'\nO Raio do Círculo é [{raio}] e a Área desse Círculo é [{area}].')

