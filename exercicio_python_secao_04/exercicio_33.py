"""
33. Leia o tamanho do lado de um quadrado e imprima como resultado a sua área.
"""

print('[Descubra a área de um quadrado!]')
lado = float(input('Digite o valor do Lado do Quadrado:'))

area = lado ** 2

print(f'\nUm quadrado com o valor de lado igual á {lado}, sua área corresponde á [{area}].')

