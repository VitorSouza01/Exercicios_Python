"""
18. Leia um valor de volume em metros cúbicos m3 e apresente-o convertido em litros.
A fórmula de conversão é: L = 1000 * M, sendo L o volume em litros e M o volume em
metrós cúbicos.
"""

print('[ CONVERSÃO DE VALOR DE VOLUME EM METROS CÚBICOS PARA LITROS]')
